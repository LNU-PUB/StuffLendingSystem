package model.lib;

/**
 * Represents a name.
 */
public class Name {
  private String name;

  /**
   * Constructor.
   *
   * @param name - The name.
   */
  public Name(String name) {
    this.name = name;
  }

  /**
   * Gets the name.
   *
   * @return - The name.
   */
  public String getName() {
    return name;
  }
}

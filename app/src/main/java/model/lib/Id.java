package model.lib;

/**
 * Represents an id.
 */
public class Id {
  private final String id;

  /**
   * Constructor.
   *
   * @param id - The id.
   */
  public Id(String id) {
    this.id = id;
  }

  /**
   * Gets the id.
   *
   * @return - The id.
   */
  public String getId() {
    return id;
  }
}
